function META:GetGlyph(str)
	local char = str:byte(1)

	if char >= 127 then return end

	local char_col = char % 16
	local char_row = math.floor(char / 16)
	local u1 = (char_col * 8) / 128
	local v1 = (char_row * 8) / 64
	local u2 = ((char_col + 1) * 8) / 128
	local v2 = ((char_row + 1) * 8) / 64
	return {
		u1 = u1,
		v1 = v1,
		u2 = u2,
		v2 = v2,
		w = 8,
		h = 8,
		x_advance = 8,
		bitmap_left = 0,
		bitmap_top = 8,
		buffer = nil, -- base font uses texture directly
		flip_y = false,
	}
end

function META:GetTextSize(str)
	str = tostring(str)
	local X, Y = 0, 8
	local max_x = 0

	for i, char in ipairs(utf8.to_list(str)) do
		if char == "\n" then
			Y = Y + 8
			max_x = math.max(max_x, X)
			X = 0
		elseif char == "\t" then
			X = X + 8 * 4
		else
			X = X + 8
		end
	end

	if max_x ~= 0 then X = max_x end

	return X, Y
end

function META:DrawString(str, x, y, spacing)
	spacing = spacing or 0
	render2d.PushTexture()
	render2d.SetTexture(self.tex)
	render2d.PushUV()
	local current_x = x
	local current_y = y

	for i = 1, #str do
		local char = str:byte(i)

		if char == 10 then
			current_x = x
			current_y = current_y + 8
		elseif char >= 0 and char <= 127 then
			local glyph = self:GetGlyph(string.char(char))
			render2d.SetUV2(glyph.u1, glyph.v1, glyph.u2, glyph.v2)
			render2d.DrawRect(current_x, current_y, glyph.w, glyph.h)
			current_x = current_x + glyph.w + spacing
		end
	end

	render2d.PopUV()
	render2d.PopTexture()
end

